% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

DATA=get(gcf,'userdata');
if ~isfield(DATA,'session')
    errdlg('no sessions selected','SessionError');
    return
elseif ~isfield(DATA,'plottype')
    errdlg('no plot type selected','PlotTypeError');
    return
end
      
if strncmpi(DATA.plottype,'By Cell No.',4)
    method=1;
elseif strncmpi(DATA.plottype,'By Distance',4)
    method=2;
elseif strncmpi(DATA.plottype,'By Cortical Depth',4)
    method=3;
elseif strncmpi(DATA.plottype,'Sig. Corr. By Depth',18)
    method=4;
elseif strncmpi(DATA.plottype,'Sig. Corr. By Distance',18)
    method=5;    
else
end

for session = 1:length(DATA.sessions)
    cd(['X:/smr/lem/',DATA.sessions(session)])
    expfiles{session}=TreeFind(cd,'.*image.ORBW.*');
    if method>3
        imors{session}=TreeFind(cd,'.*image.or.*');
    end
        
      %get noise correlations by cell  
    for j=1:length(expfiles{session})
        load(expfiles{session}{j})
        cellno=str2num(regexprep(expfiles{session}{j},'.*Cell([0-9])+.*','$1'));
        CP{cellno}=Expt.cp; 
        obstrialids=find(A==130);
        for k=1:length(obstrialids)
            if Expt.Trials(obstrialids(k)).count = 0;
                count=-1;
                fprintf('no spikes at session %s Cell %s at trialid %s',lems(session),cellno,Expt.Trials(obstrialids(k)).id);
            else
                count=Expt.Trials(obstrialids(k)).count;
            end
            countmat(cellno,Expt.Trials(obstrialids(k)).id)=count;
        end
    end
    
        if method == 2 | method == 5  %get relative distance indices for all cell pairs
            load('CellList')
            for i=1:length(cellno);
                for j=1:size(CellList,2);
                    cellprobes(j,i)=find(CellList(j,:)==cellno(i));
                end
            end
            cellprobes=mean(cellprobes);
            want=23;
            for i=1:length(cellprobes)
                for j=1:length(cellprobes)
                    celldistances(i,j) = round((cellprobes(i)-cellprobes(j))*want/23);
                end
            end
            for i=1:want+1
                h=i-1;
            [a b] = find(celldistances == h);
            CellsbyDist{i}=[a b];
            end
        end
        
%         if method == 3 | method == 4  %get depth indices for all cell
%         pairs
%             load('CellList')
%             for i=1:length(cellno);
%                 for j=1:size(CellList,2);
%                     cellprobes(j,i)=find(CellList(j,:)==cellno(i));
%                 end
%             end
%             cellprobes=mean(cellprobes);
%             want=23;
%             for i=1:length(cellprobes)
%                 for j=1:length(cellprobes)
%                     celldistances(i,j) = round((cellprobes(i)-cellprobes(j))*want/23);
%                 end
%             end
%             for i=1:want+1
%                 h=i-1;
%             [a b] = find(celldistances == h);
%             CellsbyDist{i}=[a b];
%             end
%         end
    
    %get signal correlation indices for each cell pair
 for j=1:length(expfiles{session})
        load(imors{session}{j}
            cellnosig=str2num(regexprep(imors{session}{j},'.*Cell([0-9])+.*','$1'));
            cellno=union(cellnosig,cellno);

            for i=1:length(Expt.Trials)
                A(i)=Expt.Trials(i).or
            end
                        ors=unique(A);
                        for l=1:length(ors)
                         orstrialids(l,:)=find(A==ors(l));
                        end
                        for h=1:length(obrstrialids,1)
                            for k=1:max(orstrialids,2)
                                if Expt.Trials(obstrialids(h,k)).count = 0;
                                    count=-1;
                                    fprintf('no spikes at session %s Cell %s at trialid %s',lems(session),cellno,Expt.Trials(obstrialids(h,k)).id);
                                else
                                    count=Expt.Trials(obstrialids(h,k)).count;
                                end
                                countmat(cellno,h,Expt.Trials(obstrialids(h,k)).id)=count;
                            end
                            countmat=mean(countmat,3);
                        end
        
    
    for m=1:length(Cell)
        for n=1:length(Cell)  
            corrmatrix(m,n,session)=corr(countmat([m,n],union(find(mean(countmat,2)~=0),find(countmat(n,:)~=0));
        end
    end
    corrmat=mean(corrmatrix,3));
end
end
    


